package find;

public class Pattern2 {

	public static void main(String[] args) {
		
		
	for(int a=1; a<=5; a++)     // line
	{
		for(int b = 4; b>=a; b--) // space 
		
		{
			System.out.print(" ");
		}
		for(int c = 1; c<=a; c++) //star
		{
			System.out.print(" *");
		}
		{
		System.out.println();
		
	}
	}
		
	
		
	}
}
