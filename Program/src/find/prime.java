public class prime {
	public static void main(String[] args) {
		
		 int n = 11;	 
		 int i;
	     
		 for(i=2; i<n/2; i++);
		   {
			 if(n%i==0)
			 {	
				 System.out.println("Not prime");
			 }
			 else
			 {
				 System.out.println("prime");
		     }
					 
		   }
		
	 }
	
}

